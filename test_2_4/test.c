#define _CRT_SECURE_NO_WARNINGS 1
#include<stdio.h>
#include "game.h"
#include<stdlib.h>
#include<time.h>
void menu() {
	printf("***********************\n");
	printf("*****1.play 0.exit*****\n");
	printf("***********************\n");
}
void game() {
	printf("扫雷\n");
	char mine[ROWS][COLS] = { 0 };//布置雷
	char show[ROWS][COLS] = { 0 };//排查雷的信息
	//初始化
	initBoard(mine,ROWS,COLS,'0'); 
	initBoard(show, ROWS, COLS, '*');
	//打印棋盘
	displayBoard(mine,ROW,COL);
	displayBoard(show, ROW, COL);
	//布置雷
	setMine(mine, ROW, COL);

	//扫雷
	findMine(mine,show,ROW,COL);
}
void test() {
	int input = 0;
	srand((unsigned int)time(NULL));
	do {
		menu();
		printf("请选择:>");
		scanf("%d", &input);
		switch (input) {
			case 1:
				game();
				break;
			case 0:
				printf("退出游戏\n");
				break;
			default:
				printf("选择错误，请重新选择\n");
				break;

		}
	} while (input);
}
int main() {
	test();
	return 0;

}