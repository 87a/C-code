void menu()
{
	printf("************************\n");
	printf("**** 1.play  0.exit ****\n");
	printf("************************\n");
}
//游戏算法
void game()
{
	char ret = 0;
	char board[ROW][COL];
	initBoard(board, ROW, COL);//初始化棋盘
	displayBoard(board,ROW,COL);//打印棋盘
	while (1)
	{
		//玩家下棋
		playerMove(board,ROW,COL);
		displayBoard(board, ROW, COL);
		//判断输赢
		ret = isWin(board, ROW, COL);
		if (ret != 'c')break;
		//电脑下棋
		computerMove(board, ROW, COL);
		displayBoard(board, ROW, COL);
		//判断输赢
		if (ret != 'c')break;
	}
	if (ret == '*')printf("玩家赢\n");
	else if (ret == '#')printf("电脑赢\n");
	else printf("平局\n");
}
void test()
{
	int input = 0;
	srand((unsigned int)time(NULL));
	do
	{
		menu();
		printf("请选择:>");
		scanf("%d", &input);
		switch (input)
		{
		case 1:
			printf("三子棋\n");
			game();
			break;
		case 0:
			printf("退出游戏\n");
			break;
		default:
			printf("选择错误，请重新选择\n");
			break;

		}
	} while (input);
}

int main()
{
	test();
	return 0;
}